package com.metlife.nm.cifras.vo;

public class ReenvioVO {

    private String idProceso;
    private String idEnvio;
    private String idRegistro;
    private String lob;
    private String cveProceso;
    private String medioEnvio;
    private String destinatario;
    private String subject;
    private String body;
    public final String getIdProceso() {
        return idProceso;
    }
    public final void setIdProceso(String idProceso) {
        this.idProceso = idProceso;
    }
    public final String getIdEnvio() {
        return idEnvio;
    }
    public final void setIdEnvio(String idEnvio) {
        this.idEnvio = idEnvio;
    }
    public final String getIdRegistro() {
        return idRegistro;
    }
    public final void setIdRegistro(String idRegistro) {
        this.idRegistro = idRegistro;
    }
    public final String getLob() {
        return lob;
    }
    public final void setLob(String lob) {
        this.lob = lob;
    }
    public final String getCveProceso() {
        return cveProceso;
    }
    public final void setCveProceso(String cveProceso) {
        this.cveProceso = cveProceso;
    }
    public final String getMedioEnvio() {
        return medioEnvio;
    }
    public final void setMedioEnvio(String medioEnvio) {
        this.medioEnvio = medioEnvio;
    }
    public final String getDestinatario() {
        return destinatario;
    }
    public final void setDestinatario(String destinatario) {
        this.destinatario = destinatario;
    }
    public final String getSubject() {
        return subject;
    }
    public final void setSubject(String subject) {
        this.subject = subject;
    }
    public final String getBody() {
        return body;
    }
    public final void setBody(String body) {
        this.body = body;
    }
    
    
    
}
