package com.metlife.nm.utils.calidad.vo;

public class RegistrosVO {

    private String unidadPago;
    private String retenedor;
    private String poliza;
    private String nombre;
    private String telefono;
    private String mail;
    private String refBancaria;
    private String agenteVendedor;
    private String conCobro;
    private String tipoMensaje;
    private String proceso;
    private String estatus;
    private String detalle;
    public final String getUnidadPago() {
        return unidadPago;
    }
    public final void setUnidadPago(String unidadPago) {
        this.unidadPago = unidadPago;
    }
    public final String getRetenedor() {
        return retenedor;
    }
    public final void setRetenedor(String retenedor) {
        this.retenedor = retenedor;
    }
    public final String getPoliza() {
        return poliza;
    }
    public final void setPoliza(String poliza) {
        this.poliza = poliza;
    }
    public final String getNombre() {
        return nombre;
    }
    public final void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public final String getTelefono() {
        return telefono;
    }
    public final void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    public final String getMail() {
        return mail;
    }
    public final void setMail(String mail) {
        this.mail = mail;
    }
    public final String getRefBancaria() {
        return refBancaria;
    }
    public final void setRefBancaria(String refBancaria) {
        this.refBancaria = refBancaria;
    }
    public final String getAgenteVendedor() {
        return agenteVendedor;
    }
    public final void setAgenteVendedor(String agenteVendedor) {
        this.agenteVendedor = agenteVendedor;
    }
    public final String getConCobro() {
        return conCobro;
    }
    public final void setConCobro(String conCobro) {
        this.conCobro = conCobro;
    }
    public final String getTipoMensaje() {
        return tipoMensaje;
    }
    public final void setTipoMensaje(String tipoMensaje) {
        this.tipoMensaje = tipoMensaje;
    }
    public final String getProceso() {
        return proceso;
    }
    public final void setProceso(String proceso) {
        this.proceso = proceso;
    }
    public final String getEstatus() {
        return estatus;
    }
    public final void setEstatus(String estatus) {
        this.estatus = estatus;
    }
    public final String getDetalle() {
        return detalle;
    }
    public final void setDetalle(String detalle) {
        this.detalle = detalle;
    }
    
}
